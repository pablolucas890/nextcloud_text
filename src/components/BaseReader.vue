<template>
	<EditorContent v-if="$editor" id="read-only-editor" :editor="$editor" />
</template>

<script>
import { Editor } from '@tiptap/core'
import { EditorContent } from '@tiptap/vue-2'

export default {
	name: 'BaseReader',
	components: { EditorContent },
	// extensions is a factory building a list of extensions for the editor
	inject: ['renderHtml', 'extensions'],

	props: {
		content: {
			type: String,
			required: true,
		},
	},

	computed: {
		htmlContent() {
			return this.renderHtml(this.content)
		},
	},

	watch: {
		content() {
			this.updateContent()
		},
	},

	created() {
		this.$editor = this.createEditor()
		this.$editor.setOptions({ editable: false })
	},

	beforeDestroy() {
		this.$editor.destroy()
	},

	methods: {
		createEditor() {
			return new Editor({
				content: this.htmlContent,
				extensions: this.extensions(),
			})
		},

		updateContent() {
			this.$editor.commands.setContent(this.htmlContent)
		},
	},
}
</script>
